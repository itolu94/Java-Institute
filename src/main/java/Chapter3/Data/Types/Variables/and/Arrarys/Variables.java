package main.java.Chapter3.Data.Types.Variables.and.Arrarys;

public class Variables
{
    /*
    Variables are defined by the combination of an
    identifier, a type, and an optional initializer.

    All Variables must be declared before they can be used
     */

    // Can declare variables in many ways
    int a, b, c;
    int d = 20, f, g = 39;

    // can be dynamically initialized
    int age = 10 * 3;

    /*
    variables that are declared inside of the
    scope (withing curly brackets {}) are not accessible
    outside the scope (outside of the curly brackets)

    Variables in blocked scopes are lost when the
    scope is left/ method is not being called anymore.
    */

}
